int ft_isalnum(int c)
{
	if((c >= 'a' && c <= 'z') || (c >= 'A' && c <= 'Z') 
			|| (c >= '0' && c <= '9'))
		return 1;
	return 0;
}

#include <stdio.h>

int main()
{
	char c = '3';
	char n = 'a';
	printf("%d\n", ft_isalnum(c));
	printf("%d", ft_isalnum(n));
}
